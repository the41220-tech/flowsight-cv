# 3. Non-planar 3-D: backprojection + DEM
# --------------------------------------------------------------------------- #
def test_backproject_and_dem_recovery():
    terr = Terrain(elevation_fn=ramp_basin_elevation())
    cam = PinholeCamera.look_at(
        C=(0, -10, 12), target=(0, 30, -5), f=600, width=640, height=480
    )
    xs = np.linspace(-4, 4, 5)
    ys = np.linspace(5, 34, 6)
    WX, WY = np.meshgrid(xs, ys)
    WZ = terr.elevation(WX, WY)
    P = np.column_stack([WX.ravel(), WY.ravel(), WZ.ravel()])
    uv, depthZ = cam.project(P)

    # dense-equivalent: sample exact uv with true camera-Z -> exact world
    dm = np.zeros((480, 640))
    iu = np.clip(uv[:, 0].astype(int), 0, 639)
    iv = np.clip(uv[:, 1].astype(int), 0, 479)
    dm[iv, iu] = depthZ
    rec = foot_points_to_world(cam, np.column_stack([iu, iv]).astype(float), dm)
    ref = cam.backproject_depth(np.column_stack([iu, iv]).astype(float), depthZ)
    assert np.abs(rec - ref).max() < 1e-6

    # DEM recovers terrain elevation at the sampled points
    terr2 = build_dem_from_points(P, (-6, 3, 6, 36), cell=0.5)
    samp = terr2.elevation(WX.ravel(), WY.ravel())
    assert np.abs(samp - WZ.ravel()).mean() < 0.05


