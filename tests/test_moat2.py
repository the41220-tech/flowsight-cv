# --------------------------------------------------------------------------- #
# 1. Calibration
# --------------------------------------------------------------------------- #
def test_pedestrian_scale_from_heights():
    cal = PedestrianScaleCalibrator.from_bbox_heights([80, 85, 90])  # median 85
    assert abs(cal.s - 1.7 / 85.0) < 1e-9
    # 50 px -> 1.0 m at this scale
    g = cal.to_ground([[50.0, 0.0]])[0]
    assert abs(g[0] - 50.0 * cal.s) < 1e-9
    v = cal.velocity_to_metric([[50, 0]], [[10.0, 0.0]])[0]
    assert abs(v[0] - 10.0 * cal.s) < 1e-9


def test_homography_position_and_velocity_roundtrip():
    """Crowd defined in METRES, projected to pixels, calibrated back -> recovered.

    Velocity in pixels is generated by finite differences through the inverse
    homography, so this also checks the analytic Jacobian against ground truth.
    """
    rng = np.random.default_rng(0)
    # image corners -> a perspective-distorted ground quad (metres)
    img = [[0, 0], [640, 0], [640, 480], [0, 480]]
    wld = [[0, 0], [12, 0], [10, 9], [1, 8]]
    cal = HomographyCalibrator.from_points(img, wld)
    Hinv = np.linalg.inv(cal.H)

    def ground_to_img(xy):
        xy = np.atleast_2d(xy)
        hom = np.hstack([xy, np.ones((len(xy), 1))]) @ Hinv.T
        return hom[:, :2] / hom[:, 2:3]

    xy_m = rng.uniform([1, 1], [9, 7], size=(40, 2))
    vel_m = rng.normal(0, 0.8, size=(40, 2))
    dt = 1e-3
    uv0 = ground_to_img(xy_m)
    uv1 = ground_to_img(xy_m + vel_m * dt)
    vel_px = (uv1 - uv0) / dt

    rec_xy = cal.to_ground(uv0)
    rec_vel = cal.velocity_to_metric(uv0, vel_px)
    assert np.abs(rec_xy - xy_m).max() < 1e-6
    assert np.abs(rec_vel - vel_m).max() < 1e-3  # finite-diff vs analytic Jacobian


# --------------------------------------------------------------------------- #
# 2. Absolute pressure UNITS — the crux of the moat
# --------------------------------------------------------------------------- #
def test_absolute_pressure_units():
    """Uniform crowd of known density & velocity variance -> P = rho*Var in 1/s^2.

    rho = 4 persons/m^2 ; per-axis vel var 0.09 -> Var(v) = 0.18 (m/s)^2
    => expected P ~ 0.72 /s^2 (well above the 0.02 Helbing threshold).
    """
    rng = np.random.default_rng(3)
    L = 20.0
    rho_target = 4.0
    N = int(rho_target * L * L)  # 1600
    xy = rng.uniform(0, L, size=(N, 2))
    vel = rng.normal(0, 0.3, size=(N, 2))  # per-axis var 0.09
    fld = frame_pressure_metric(xy, vel, (0, 0, L, L), cell_m=0.5, sigma_m=1.0)

    # interior cells only (avoid constant-mode smoothing fall-off at edges)
    gh, gw = fld["pressure"].shape
    sl = (slice(gh // 4, 3 * gh // 4), slice(gw // 4, 3 * gw // 4))
    rho_med = float(np.median(fld["density"][sl]))
    var_med = float(np.median(fld["var_v"][sl]))
    p_med = float(np.median(fld["pressure"][sl]))

    assert abs(rho_med - 4.0) < 0.8, rho_med            # persons/m^2
    assert abs(var_med - 0.18) < 0.06, var_med          # (m/s)^2
    assert abs(p_med - 0.72) < 0.25, p_med              # 1/s^2
    # P == rho*Var elementwise (definition holds)
    assert np.allclose(fld["pressure"], fld["density"] * fld["var_v"], atol=1e-6)


def test_alarm_thresholds():
    assert alarm_level(0.005)["severity"] == "safe"
    assert alarm_level(0.010)["severity"] == "caution"  # boundary 0.5*crit
    assert alarm_level(0.015)["severity"] == "caution"
    assert alarm_level(0.020)["severity"] == "danger"   # boundary crit
    assert alarm_level(0.050)["severity"] == "danger"
    assert abs(alarm_level(0.04)["frac"] - 2.0) < 1e-9  # 0.04 / 0.02


