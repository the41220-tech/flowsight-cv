# 4. Gravitational potential: downhill force direction & magnitude
# --------------------------------------------------------------------------- #
def test_potential_downhill_force():
    # ramp: z = -tan(theta)*(y - y0) for y>y0 -> slopes DOWN toward +y
    theta = np.deg2rad(20.0)
    terr = Terrain(elevation_fn=ramp_basin_elevation(y0=10.0, theta_deg=20.0,
                                                     basin_y=999, basin_depth=0))
    x = np.array([0.0, 0.0])
    y = np.array([20.0, 25.0])  # on the ramp
    Fx, Fy, Fmag = potential.potential_gradient(terr, x, y)
    assert np.all(Fy > 0)                       # force points downhill (+y)
    assert np.all(np.abs(Fx) < 1e-6)            # no cross-slope component
    expected = potential.MASS * potential.G * np.tan(theta)
    assert np.all(np.abs(Fmag - expected) < 0.5 * expected)  # ~ m g tan(theta)


def test_moat_monitor_terrain_channel_active_on_slope():
    terr = Terrain(elevation_fn=ramp_basin_elevation())
    bounds = (-6, 3, 6, 36)
    rng = np.random.default_rng(1)
    N = 300
    xy = np.column_stack([rng.uniform(-3, 3, N), rng.uniform(28, 34, N)])
    vel = rng.normal(0, 0.3, size=(N, 2))
    mon = MoatMonitor(bounds, terrain=terr, cell_m=0.5, sigma_m=1.0)
    r = mon.step_metric(xy, vel)
    assert r["p_max"] > P_CRIT                 # dense erratic crowd -> danger
    assert r["alarm"]["severity"] == "danger"
    assert r["terrain_push_max"] > 0           # non-planar channel is active
    assert r["Fmag"].max() > 0


